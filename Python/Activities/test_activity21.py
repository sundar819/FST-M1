import pytest

def test_sum():
   n1=20
   n2=10
   add1=n1+n2
   assert(add1==30)


def test_sub():
    n1=20
    n2=10
    sub1=n1-n2
    assert(sub1==10)


def test_mul():
    n1=20
    n2=10
    mul1=n1*n2
    assert(mul1==200)


def test_div():
    n1=20
    n2=10
    div1=n1/n2
    assert(div1==2)

 